    <p>A Fundação Terra foi criada por Pe. Airton Freire há 37 anos, no dia 08 de setembro de 1984, em uma comunidade popularmente conhecida como “Rua do Lixo” e que se localiza nos arredores do depósito de resíduos sólidos (“Lixão”) da cidade de Arcoverde, no semi-árido do Estado de Pernambuco. Nasceu nessa comunidade com o intuito de resgatar, literalmente, do lixo, homens, mulheres e crianças, identificados como o supra-sumo da miséria da cidade de Arcoverde. Foi fundada para servir aos pobres, no sentido stricto (do quotidiano) e no sentido lato (sistêmico). Possui como lema: “Na Terra para Servir”; servir aos pobres, por meio da educação, saúde e ações sociais, com vistas à superação da exclusão social e promoção da dignidade, perpetuando o carisma de, indistintamente, fazer o bem. É uma entidade civil de confissão Católica Apostólica Romana, sem fins lucrativos, e filantrópica, que possui certificado de utilidade pública municipal, estadual e federal.</p>

    <p>Antes do trabalho da Fundação da Terra, a Rua do Lixo era uma área na periferia da cidade onde cerca de 200 famílias viviam reciclando o lixo do “Lixão” da cidade, enquanto outras pessoas viviam quebrando pedras em pedaços menores para a indústria de construção. Juntos, porcos e crianças procuravam entre o lixo por comida. Como resultado dessas condições desumanas, as crianças enfrentavam uma realidade de prostituição, alcoolismo, drogas e abortos. Ao longo dos anos, muitas coisas maravilhosas aconteceram e a Rua do Lixo se transformou. Por meio dos projetos da Fundação Terra, a comunidade começou a crescer na Rua do Lixo, e a realidade das pessoas mudou para melhor.</p>

